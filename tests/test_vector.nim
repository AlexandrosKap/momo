import unittest
import momo/vector

test "Vector":
  check vec2(2, 2) == Vec2(x: 2, y: 2)
  check vec2(1) == vec2(1, 1)
  check vec2() == vec2(0)
  check vec2(4) + vec2(2) == vec2(6)
  check -vec2(2) == vec2(-2)
  check vec2(4) - vec2(2) == vec2(2)
  check vec2(4) * vec2(2) == vec2(8)
  check vec2(4) / vec2(2) == vec2(2)
  check vec2(4) div vec2(2) == vec2(2)
  check vec2(4) mod vec2(2) == vec2(0)
  check vec2(4) * 2 == vec2(8)
  check vec2(4) * 0.5 == vec2(2)
  check vec2(4) / 2 == vec2(2)
  check vec2(4) / 2.0 == vec2(2)

  check vec3(2, 2, 2) == Vec3(x: 2, y: 2, z: 2)
  check vec3(1) == vec3(1, 1, 1)
  check vec3() == vec3(0)
  check vec3(4) + vec3(2) == vec3(6)
  check -vec3(2) == vec3(-2)
  check vec3(4) - vec3(2) == vec3(2)
  check vec3(4) * vec3(2) == vec3(8)
  check vec3(4) / vec3(2) == vec3(2)
  check vec3(4) div vec3(2) == vec3(2)
  check vec3(4) mod vec3(2) == vec3(0)
  check vec3(4) * 2 == vec3(8)
  check vec3(4) * 0.5 == vec3(2)
  check vec3(4) / 2 == vec3(2)
  check vec3(4) / 2.0 == vec3(2)

  check vec4(2, 2, 2, 2) == Vec4(x: 2, y: 2, z: 2, w: 2)
  check vec4(1) == vec4(1, 1, 1, 1)
  check vec4() == vec4(0)
  check vec4(4) + vec4(2) == vec4(6)
  check -vec4(2) == vec4(-2)
  check vec4(4) - vec4(2) == vec4(2)
  check vec4(4) * vec4(2) == vec4(8)
  check vec4(4) / vec4(2) == vec4(2)
  check vec4(4) div vec4(2) == vec4(2)
  check vec4(4) mod vec4(2) == vec4(0)
  check vec4(4) * 2 == vec4(8)
  check vec4(4) * 0.5 == vec4(2)
  check vec4(4) / 2 == vec4(2)
  check vec4(4) / 2.0 == vec4(2)

  var v2 = vec2(1)
  v2 += vec2(2)
  check v2 == vec2(3)
  check v2.ivec2 == ivec2(3)

  var v3 = vec3(1)
  v3 += vec3(2)
  check v3 == vec3(3)
  check v3.ivec3 == ivec3(3)

  var v4 = vec4(1)
  v4 += vec4(2)
  check v4 == vec4(3)
  check v4.ivec4 == ivec4(3)
